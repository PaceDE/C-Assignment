// Program to convert Decimal to Hexadecimal and Octal Number

#include<stdio.h>
void main()
{
    int a, b, c;
    a=10;
    b=5;
    c= a+b;
    printf("\n Decimal no = %d", c);
    printf("\n Decimal to Octal no = %o", c);
    printf("\n Decimal to Hexadecimal no = %x", c);
    printf("\n Decimal to Hexadecimal no = %X", c);

}
